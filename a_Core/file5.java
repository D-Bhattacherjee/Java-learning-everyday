public class file5 {
    public static void main(String[] args) {
        // looping with for loop
        int limit = 10;

        // printing from 0 to 10
        /*
         * for(initiation of variable; run untill condition; increase or decreasevalue){
         * code-to-execute
         * }
         */
        for (int i = 0; i <= limit; i++) {
            System.out.println(i);
        }
        System.out.println("=============================");
        // another loop While
        int j = 0;
        while (j <= 10) {

            j += 1; // it means j = j + 1
            System.out.println(j);
        }

    }
}
