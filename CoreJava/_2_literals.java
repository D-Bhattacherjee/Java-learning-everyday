package CoreJava;

public class _2_literals {
    public static void main(String[] args) {
        // Literals
        int num1 = 9;
        int num2 = 0b01101;
        int num3 = 0xE;
        int num4 = 9;

        System.out.println("num1" + num1);
        System.out.println("num2" + num2);
        System.out.println("num3" + num3);
        System.out.println(num4);
    }
}
