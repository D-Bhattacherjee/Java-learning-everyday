package CoreJava;

public class _5_loops1 {
    public static void main(String[] args) {

        int count = 0;
        while (count < 10) {
            count++;
            System.out.println(count);
        }

        for (int i = 0; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
